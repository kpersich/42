#include <unistd.h>

void	ft_print(int a, int b)
{
	char x[5];

	x[0] = '0' + (a / 10);
	x[1] = '0' + (a % 10);
	x[2] = ' ';
	x[3] = '0' + (b / 10);
	x[4] = '0' + (b % 10);
	write(1, x, 5);
	if (x[0] != '9' || x[1] != '8' || x[3] != '9' || x[4] != '9')
		write(1, ", ", 2);
}

void	ft_print_comb2(void)
{
	int a;
	int b;

	a = 0;
	while (a <= 98)
	{
		b = (a + 1);
		while (b <= 99)
		{
			ft_print(a, b);
			b++;
		}
		a++;
	}
}
