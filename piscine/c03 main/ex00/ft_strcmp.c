int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int somma;

	i = 0;
	while (s1[i] != '\0')
	{
		somma = s1[i] - s2[i];
		if (somma < 0)
			break ;
		if (somma > 0)
			break ;
		i++;
	}
	return (somma);
}
