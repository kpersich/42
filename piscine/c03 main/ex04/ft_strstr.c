char	*ft_strstr(char *str, char *to_find)
{
	int s;
	int t;

	s = 0;
	while (str[s] != '\0')
	{
		t = 0;
		while (str[s + t] == to_find[t])
		{
			if (to_find[t + 1] == '\0')
				return (&str[s]);
			t++;
		}
		s++;
	}
	return (0);
}
