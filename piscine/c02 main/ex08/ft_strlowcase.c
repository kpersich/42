char	*ft_strlowcase(char *str)
{
	int s;

	s = 0;
	while (str[s] != '\0')
	{
		if (str[s] >= 'A' && str[s] <= 'Z')
		{
			str[s] = str[s] + 32;
		}
		s++;
	}
	return (str);
}
